def __blinkEyes():
    __startFunction()
    __drawEyes()
    openeyes = turtle.Turtle("OpenedEyes")
    openeyes.speed(0)
    openeyes.penup()
    openeyes.left(90)
    openeyes.goto(-5, 20)
    openeyes.resizemode("user")
    openeyes.shapesize(2, 3)
    closeeyes = turtle.Turtle("ClosedEyes")
    closeeyes.speed(0)
    closeeyes.penup()
    closeeyes.left(90)
    closeeyes.goto(-33, 57)
    closeeyes.resizemode("user")
    closeeyes.shapesize(2, 3)
    while 1:
        sleep(4)
        openeyes.hideturtle()
        closeeyes.showturtle()
        sleep(0.1)
        openeyes.showturtle()

def __talk():
    __startFunction()
    __drawMouth()
    lowerlips = turtle.Turtle("LowerLips")
    lowerlips.speed(0)
    lowerlips.penup()
    lowerlips.goto(-100, -130)
    lowerlips.left(60)
    upperlips = turtle.Turtle("UpperLips")
    upperlips.color("cyan")
    upperlips.speed(0)
    upperlips.penup()
    upperlips.goto(-179, 45)
    while 1:
        sleep(0.1)
        upperlips.hideturtle()
        sleep(0.1)
        upperlips.showturtle()

def normalEmotion():
    __startEmotion()
    __showMouth()
    __showNose()
    __showEyeBrows()
    __blinkEyes()
    __endEmotion()

def talkingEmotion():
    __startEmotion()
    __showEyes()
    __showEyeBrows()
    __showNose()
    __talk()
    __endEmotion()


