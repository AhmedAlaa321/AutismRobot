from turtle import Turtle, Screen, Shape
from time import sleep
import turtle
screen = turtle.Screen()
cursor = turtle.Turtle()

def initialize():
    turtle.setup(800, 484)
    turtle.bgcolor("cyan")
    cursor.hideturtle()

def __startEmotion():
    turtle.reset()
    turtle.speed(0)

def __endEmotion():
    cursor.hideturtle()
    turtle.done()

def __startFunction():
    turtle.hideturtle()
    turtle.speed(0)
    turtle.penup()
    turtle.goto(0, 0)

def __drawEndFunction():
    turtle.reset()

def __drawEyes():
    __startFunction()
    openeyes = Shape("compound")
    turtle.begin_poly()
    turtle.goto(-80, 0)
    turtle.pendown()
    turtle.circle(15)
    turtle.penup()
    turtle.end_poly()
    lefteyeoutframe = turtle.get_poly()
    openeyes.addcomponent(lefteyeoutframe, "black", "cyan")
    turtle.begin_poly()
    turtle.penup()
    turtle.goto(-80, 0)
    turtle.pendown()
    turtle.circle(8)
    turtle.penup()
    turtle.end_poly()
    lefteyeinframe = turtle.get_poly()
    openeyes.addcomponent(lefteyeinframe, "cyan", "black")
    turtle.begin_poly()
    turtle.penup()
    turtle.goto(80, 0)
    turtle.pendown()
    turtle.circle(15)
    turtle.penup()
    turtle.end_poly()
    righteyeoutframe = turtle.get_poly()
    openeyes.addcomponent(righteyeoutframe, "black", "cyan")
    turtle.begin_poly()
    turtle.penup()
    turtle.goto(80, 0)
    turtle.pendown()
    turtle.circle(8)
    turtle.penup()
    turtle.end_poly()
    righteyeinframe = turtle.get_poly()
    openeyes.addcomponent(righteyeinframe, "cyan", "black")
    screen.addshape("OpenedEyes", openeyes)
    closeeyes = Shape("compound")
    turtle.begin_poly()
    turtle.goto(-80, 0)
    turtle.pendown()
    turtle.fd(28)
    turtle.left(90)
    turtle.fd(2)
    turtle.left(90)
    turtle.fd(28)
    turtle.left(90)
    turtle.fd(2)
    turtle.end_poly()
    leftcloseeye = turtle.get_poly()
    closeeyes.addcomponent(leftcloseeye, "black", "cyan")
    turtle.penup()
    turtle.begin_poly()
    turtle.goto(80, 0)
    turtle.pendown()
    turtle.left(90)
    turtle.fd(28)
    turtle.left(90)
    turtle.fd(2)
    turtle.left(90)
    turtle.fd(28)
    turtle.left(90)
    turtle.fd(2)
    turtle.end_poly()
    rightcloseeye = turtle.get_poly()
    closeeyes.addcomponent(rightcloseeye, "black", "cyan")
    screen.addshape("ClosedEyes", closeeyes)
    __drawEndFunction()

def __showEyes():
    __startFunction()
    __drawEyes()
    eyes = turtle.Turtle("OpenedEyes")
    eyes.speed(0)
    eyes.penup()
    eyes.left(90)
    eyes.goto(-5, 20)
    eyes.resizemode("user")
    eyes.shapesize(2, 3)

def __drawMouth():
    __startFunction()
    turtle.color("black", "black")
    turtle.begin_poly()
    turtle.begin_fill()
    turtle.pensize(10)
    turtle.circle(200, 60)
    turtle.end_fill()
    turtle.end_poly()
    lowerlips = turtle.get_poly()
    screen.addshape("LowerLips", lowerlips)
    turtle.color("cyan", "cyan")
    turtle.pensize(10)
    turtle.begin_poly()
    turtle.begin_fill()
    turtle.circle(160, 60)
    turtle.end_fill()
    turtle.end_poly()
    upperlips = turtle.get_poly()
    screen.addshape("UpperLips", upperlips)
    __drawEndFunction()

def __showMouth():
    __startFunction()
    __drawMouth()
    lowerlips = turtle.Turtle("LowerLips")
    lowerlips.speed(0)
    lowerlips.penup()
    lowerlips.goto(-100, -130)
    lowerlips.left(60)
    upperlips = turtle.Turtle("UpperLips")
    upperlips.color("cyan")
    upperlips.speed(0)
    upperlips.penup()
    upperlips.goto(-179, 45)

